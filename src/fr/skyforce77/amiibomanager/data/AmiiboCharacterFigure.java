	private byte[] code;
	
	AmiiboCharacterFigure(byte[] code) {
		this.code = code;
	}
	
	public byte[] getCode() {
		return code;
	}

	private void setCode(byte[] code) {
		this.code = code;
	}
	
	@Override
	public String toString() {
		if(this == UNKNOWN) {
			StringBuilder str = new StringBuilder("UNKNOWN (");
			for(int i = 0; i < 4; i++) {
				str.append("0x"+String.format("%02x",code[i]));
				if(i != 3) {
					str.append(", ");
				} else {
					str.append(")");
				}
			}
			return str.toString();
		} else {
			return this.name();
		}
	}
	
	public static AmiiboCharacterFigure fromPage(byte[] page) {
		for(AmiiboCharacterFigure type: values()) {
			if(Arrays.equals(type.getCode(), page)) {
				return type;
			}
		}
		UNKNOWN.setCode(page);
		return UNKNOWN;
	}
	
